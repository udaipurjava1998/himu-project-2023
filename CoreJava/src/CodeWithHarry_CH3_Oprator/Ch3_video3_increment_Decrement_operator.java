package CodeWithHarry_CH3_Oprator;

public class Ch3_video3_increment_Decrement_operator {
	public static void main(String[] args) {
		// increment operator  		
		int a = 5;
		int b = ++a *2;
		
		// answer is = 12
		
		System.out.println(b);
		
		// Decrement operator
		
		int c = 10;
		int d = --c *3;
		
		// answer =  27
				
				System.out.println(d);

		
	}	

}
